package com.anas.fdbauthxprofile;

public class MODEL {

    String NAME,AGE;

    public MODEL(String NAME, String AGE) {
        this.NAME = NAME;
        this.AGE = AGE;
    }

    public String getNAME() {
        return NAME;
    }

    public void setNAME(String NAME) {
        this.NAME = NAME;
    }

    public String getAGE() {
        return AGE;
    }

    public void setAGE(String AGE) {
        this.AGE = AGE;
    }
}
